namespace ReleaseLib
{
    /// <summary>
    /// Формат носителя.
    /// </summary>
    public enum MediumFormat
    {
        /// <summary>
        /// CD-диск.
        /// </summary>
        CD = 0,
        /// <summary>
        /// Виниловая пластинка.
        /// </summary>
        Vinyl = 1,
        /// <summary>
        /// Аудиокассетта.
        /// </summary>
        Cassette = 2,
        /// <summary>
        /// Цифровой носитель (например, mp3-файл).
        /// </summary>
        DigitalMedia = 3,
        /// <summary>
        /// DVD-диск.
        /// </summary>
        DVD = 4,
        /// <summary>
        /// SACD-диск (Super Audio CD).
        /// </summary>
        SACD = 5,
        /// <summary>
        /// Минидиск (MD).
        /// </summary>
        MiniDisc = 6,
        /// <summary>
        /// Прочее.
        /// </summary>
        Other = 7
    }
}
